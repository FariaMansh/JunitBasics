package com.cogent;

public class NumberCalculation {
	
	//Addition
	public int add(int x, int y) {
		return x + y;
	}
	
	//subtraction
	public int subtract(int x, int y) {
		return x - y;
	}
	
	//Multiply
	public int multiply(int x, int y) {
		return x * y;
	}
	
	// divide
	public int divide(int x, int y) {
		return x/y;
	}

}
